void main() {
    // Number memiliki 2 jenis tipe data
    // pertama adalah int -> untuk bilangan bulat
    // kedua adalah double -> untuk bilangan desimal

    int number1 = 20;
    double number2 = 20.5;

    // untuk tipe data terakhir adalah num,
    // tipe data num, bisa berisi int atau double
    num number3 = 20;
    print(number3);

    number3 = 20.5;
    print(number3);
}